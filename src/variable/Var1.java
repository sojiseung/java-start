package variable;

public class Var1 {

    public static void main(String[] args) {
        int x;  // 변수 선언
        x = 15; // 변수 초기화

        int x1 =20;  // 변수 선언 및 초기화를 한번에 가능
        String y = "자바 왕초보";    // 문자열 변수

        System.out.println(x);
        System.out.println(y);
        System.out.println(x1);
    }
}
